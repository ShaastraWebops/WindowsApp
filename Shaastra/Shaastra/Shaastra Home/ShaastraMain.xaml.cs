// The Blank Page item template is documented at http://go.microsoft.com/fwlink/?LinkID=390556

namespace Shaastra.Shaastra_Home
{
    /// <summary>
    /// An empty page that can be used on its own or navigated to within a Frame.
    /// </summary>
    public sealed partial class ShaastraMain : Page
    {
        public ShaastraMain()
        {
            this.InitializeComponent();

            DrawerLayout.InitializeDrawerLayout();

            string[] menuItems = new string[7] { "Events", "Workshops", "Shows", "Lectures", "Schedules", "Sponsors", "Contact Us" };
            ListMenuItems.ItemsSource = menuItems.ToList();
        }

        /// <summary>
        /// Invoked when this page is about to be displayed in a Frame.
        /// </summary>
        /// <param name="e">Event data that describes how this page was reached.
        /// This parameter is typically used to configure the page.</param>

        protected override void OnNavigatedTo(NavigationEventArgs e)
        {
            Windows.Phone.UI.Input.HardwareButtons.BackPressed += HardwareButtons_BackPressed;
        }

        void HardwareButtons_BackPressed(object sender, Windows.Phone.UI.Input.BackPressedEventArgs e)
        {
            if (DrawerLayout.IsDrawerOpen)
            {
                DrawerLayout.CloseDrawer();
                e.Handled = true;
            }
            else
            {
                Application.Current.Exit();
            }
        }

        private void DrawerIcon_Tapped(object sender, TappedRoutedEventArgs e)
        {
            if (DrawerLayout.IsDrawerOpen)
                DrawerLayout.CloseDrawer();
            else
                DrawerLayout.OpenDrawer();
        }

    }
}
